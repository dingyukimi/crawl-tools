# include <stdio.h>


int main ()
{
    int T, n, i, j ;
    scanf ("%d", &T) ;
    while (T--)
    {
        scanf ("%d", &n) ;
        for (i = 0 ; i < n*3 ; i++)
        {
            for (j = 0 ; j < n ; j++)
                printf ("HDU") ;
            printf ("\n") ;
        }
    }
    return 0 ;
}