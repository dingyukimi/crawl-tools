#include<stdio.h>
int ans[500001];
void fun()
{
    int i,j;
    for(i=1;i<=500000;i++)
        ans[i]=1;
    for(i=2;i<=250000;i++)
    {
        for(j=2*i;j<=500000;j+=i)
            ans[j]+=i;
    }
}
int main()
{
    int t,n;
    fun();
    while(scanf("%d",&t)!=EOF)
    {
        while(t--)
        {
        scanf("%d",&n);
        printf("%d\n",ans[n]);
        }
    }
    return 0;
}