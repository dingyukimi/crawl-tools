#include <stdio.h>
#include <string.h>
#include <algorithm>
#include <iostream>
using namespace std;
#define LL __int64
const LL mod=1e9+7;
LL a[1000001],b[1000001];
LL pows(LL a,LL b)
{
    LL s=1;
    a=a%mod;
    while(b)
    {
        if(b&1)
            s=(s*a)%mod;
        a=(a*a)%mod;
        b=b>>1;
    }
    return s;
}
int main()
{
    LL T;
    scanf("%I64d",&T);
    while(T--)
    {
        LL n,i,j,k,mul=1,p;
        scanf("%I64d",&n);
        for(i=0;i<n;i++)
        {
            scanf("%I64d",&a[i]);
            mul=(a[i]*mul)%mod;
        }
        sort(a,a+n);
        for(i=0,j=0;i<n;i=i+2,j++)
            b[i]=a[j];
        for(i=(n%2==0?n-1:n-2),k=j;i>=0;k++,i=i-2)
            b[i]=a[k];
        LL s=0;
        for(i=1;i<n;i++)
        {
            p=(mul*pows(b[i]*b[i-1],mod-2))%mod;
            s=(s+min(b[i],b[i-1])*p%mod)%mod;
        }
        s=(mul*n-s+mod)%mod;
        printf("%I64d\n",s);
    }
    return 0;
}
