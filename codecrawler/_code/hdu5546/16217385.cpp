#include<stdio.h>
#include<string.h>
#include<algorithm>
using namespace std;
#define MS(x,y) memset(x,y,sizeof(x))
#define MC(x,y) memcpy(x,y,sizeof(x))
typedef long long LL;
const int Z=1e9+7;
const int N=12;
const int dy[4]={-1,0,0,1};
const int dx[4]={0,-1,1,0};
int casenum,casei;
int n,m;
char a[N][N];
int e[N][N];
int tim,cnt;
void dfs(int y,int x)
{
    e[y][x]=1;
    for(int i=0;i<4;i++)
    {
        int yy=y+dy[i];
        int xx=x+dx[i];
        if(a[yy][xx]=='.'&&e[yy][xx]!=tim){++cnt;e[yy][xx]=tim;}
        if(a[yy][xx]=='o'&&e[yy][xx]==0)dfs(yy,xx);
    }
}
bool check()
{
    MS(e,0);
    for(int i=1;i<=9;i++)
    {
        for(int j=1;j<=9;j++)if(a[i][j]=='o'&&e[i][j]==0)
        {
            ++tim;
            cnt=0;
            dfs(i,j);
            if(cnt==1)return 1;
        }
    }
    return 0;
}
int main()
{
    scanf("%d",&casenum);
    for(casei=1;casei<=casenum;casei++)
    {
        for(int i=1;i<=9;i++)scanf("%s",a[i]+1);
        if(check())printf("Case #%d: Can kill in one move!!!\n",casei);
        else printf("Case #%d: Can not kill in one move!!!\n",casei);
    }
    return 0;
}
