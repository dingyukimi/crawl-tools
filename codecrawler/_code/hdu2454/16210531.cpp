#include<iostream>
#include<cstdio>
#include<cstdlib>
#include<algorithm>
#include<cstring>
using namespace std;

const int N=1010;
int a[N];

bool cmp(int x , int y)
{
    return x>y;
}

int main()
{
    int t;
    scanf("%d" , &t);
    while(t--)
    {
        int n ;
        scanf("%d" , &n);
        int sum = 0;
        for(int i = 0 ; i < n ; i++)
        {
            scanf("%d" , &a[i]);
            sum += a[i];
        }
        if(sum%2)
        {
            printf("no\n");
            continue;
        }

        int flag=0;
        for(int i = 0 ; i < n ; i++)
        {
            sort(a , a + n , cmp);
            if(a[0] == 0)
            {
                flag = 1;
                break;
            }
            for(int j = 0 ; j < a[0] ; j++)
            {
                a[j+1]--;
                if(a[j+1] < 0)
                {
                    flag = 2;
                    break;
                }
            }
            a[0] = 0;
            if(flag == 2)
                break;
        }
        if(flag == 1)
            printf("yes\n");
        else
            printf("no\n");
    }
    return 0;
}
