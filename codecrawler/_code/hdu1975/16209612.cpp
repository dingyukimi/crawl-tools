#include <iostream>
#include <string.h>
using namespace std;
int c[30],vis[30];
char a[30];
int main()
{
    int T;
    cin>>T;
    while(T--)
    {
        int i,j,k,t,flag=0;
        cin>>a;
        memset(c,0,sizeof(c));
        memset(vis,0,sizeof(vis));
        for(i=0;i<26;i++)
        {
            if(vis[i])continue;
            t=1;
            vis[i]=1;
            j=i;
            while(a[j]-'A'!=i)
            {
                t++;
                j=a[j]-'A';
                vis[j]=1;
            }
            c[t]++;
        }
        for(i=2;i<=26;i+=2)
        if(c[i]%2==1){flag=1;break;}
        if(flag)cout<<"No"<<endl;
        else cout<<"Yes"<<endl;
    }
}
